a = 10
b = 13

# AND : &
print(bin(a), "a: {}".format(a))
print(bin(b), "b: {}".format(b))
print(bin(a&b), "a&b: {}\n".format(a&b))

# OR: |
print(bin(a), "a: {}".format(a))
print(bin(b), "b: {}".format(b))
print(bin(a|b), "a|b: {}\n".format(a|b))


# XOR:  ^
print(bin(a), "a: {}".format(a))
print(bin(b), "b: {}".format(b))
print(bin(a^b), "a^b: {}\n".format(a^b))
